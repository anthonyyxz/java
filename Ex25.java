/******************************************************************************
Faça um programa, utilizando while, que mostre na tela os números de 0 a 100.
*******************************************************************************/
public class Ex25
{
	public static void main(String[] args) {
		int numero = 0;
		
		while (numero <= 99){
		    numero ++;
		System.out.println("Número " + numero);
		}
	}
}
